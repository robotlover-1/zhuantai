/**
 ****************************************************************************************************
 * @file        app_util.h
 * @brief       应用层通用工具函数（消除重复代码）
 ****************************************************************************************************
 */
#ifndef __APP_UTIL_H
#define __APP_UTIL_H

#include "sys.h"

/******************************************************************************************/
/* 报警/状态指示 */
void alarm_pulse(void);                 /* 报警IO闪烁(ALARM引脚输出50ms脉冲) */

void done_pulse(void);                  /* 完成IO脉冲(DONE引脚输出100ms脉冲) */

/******************************************************************************************/
/* 光电传感器 */
int  ele_is_triggered(void);            /* 光电传感器消抖读取, 返回1=触发, 0=未触发 */

/******************************************************************************************/
/* 电机安全控制 */
void motor_safe_stop(void);             /* 紧急安全停止(停止PWM+清标志+停TIM5) */

void motor_stop_normal(void);           /* 正常停止(成功路径: 安全停止+置printf_flag) */

void motor_start_motion(void);          /* 标准电机启动序列(清零time_out+启动TIM5+PID+启动电机) */

/******************************************************************************************/
/* 电机运动等待 */
void wait_motion_done(void);            /* 等待电机运行结束(run_printf_flag==0) */

int  wait_motion_done_or_error(void);   /* 等待运行结束或出错，返回error状态 */

/******************************************************************************************/
/* 运行中异常检测(在while循环中调用, 返回1表示需break) */
int  check_24v_loss(void);              /* 24V断电检测, 断电自动停止电机并返回1 */

int  check_motion_timeout(int alarm_code, const char *msg); /* 超时检测, 超时自动停止并返回1 */

/******************************************************************************************/
/* 串口工具 */
int  parse_uart_number(u8 *buf, int len); /* 解析串口接收的数字参数 */

void trigger_photo(void);                 /* 触发拍照(IO输出+UART4发"OP") */

/******************************************************************************************/
/* Flash工具 */
void flash_save_param(uint32_t addr, int32_t data); /* 保存参数到Flash */

/******************************************************************************************/
/* 工位验证 */
int  check_station_valid(int hal_start, int hal_end);  /* 工位到达验证，返回1=OK, 0=FAIL */

void handle_station_verify(int hal_start, int hal_end); /* 工位验证+完成/报警输出 */
#endif
